                                        ? null
                                        : _importDocument,
                                  ),
                                // Always show the mic button — even if
                                // the browser doesn't support voice input.
                                if (widget.enableAi) ...[
                                  if (_isListening)
                                    Container(
                                      width: 32,
                                      height: 32,
                                      margin: const EdgeInsets.only(right: 4),
                                      decoration: BoxDecoration(
